package com.vechicledekho;

public class VechicleProperties {
	
	private String vechicleModalName;
	private long vechiclePrice;
	private String fuelType;
	private String vechicleCompany;
	private String vechicleColor;	
	
	
	public String getVechicleModalName() {
		return vechicleModalName;
	}
	public void setVechicleModalName(String vechicleModalName) {
		this.vechicleModalName = vechicleModalName;
	}
	public long getVechiclePrice() {
		return vechiclePrice;
	}
	public void setVechiclePrice(long vechiclePrice) {
		this.vechiclePrice = vechiclePrice;
	}
	public String getFuelType() {
		return fuelType;
	}
	public void setFuelType(String fuelType) {
		this.fuelType = fuelType;
	}
	public String getVechicleCompany() {
		return vechicleCompany;
	}
	public void setVechicleCompany(String vechicleCompany) {
		this.vechicleCompany = vechicleCompany;
	}
	
	public String getVechicleColor() {
		return vechicleColor;
	}
	public void setVechicleColor(String vechicleColor) {
		this.vechicleColor = vechicleColor;
	}

}
